#Ejercicio 1
print("Ejercicio 1: operaciones aritmeticas")

#Entradas
numero1 = int(input("Ingresa un numero"))
numero2 = int(input("Ingresa otro un numero"))

#Operaciones
suma = numero1 + numero2
resta = numero1 - numero2
divisionEntera = numero1 // numero2
divisionModular = numero1 % numero2
multiplicacion = numero1 * numero2

#Salidas
print(numero1, "//", numero2, "=" ,divisionEntera)
print(numero1, "%", numero2, "=" ,divisionModular)
print("Suma" ,suma)
print("Resta" ,resta)
print("Multiplicacion" ,multiplicacion)


#Ejercicio 2
print("Ejercicio 2: operaciones boleanas")
diferencia = numero1 != numero2
print(numero1, "!=", numero2, "=", diferencia)


